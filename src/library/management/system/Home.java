    private void btnAddBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddBookActionPerformed

        String query = "SELECT role FROM login WHERE userid = ?";
    try (PreparedStatement pst = con.prepareStatement(query)) {
    
        pst.setString(1, userid);

        try (ResultSet rs = pst.executeQuery()) {
            if (rs.next()) {
                String role = rs.getString("role");
                if ("admin".equalsIgnoreCase(role)) {
                    new AddBook().setVisible(true); // Allow admin to proceed
                } else {
                    JOptionPane.showMessageDialog(null, "Error: You are not admin.", "Access Denied", JOptionPane.ERROR_MESSAGE);
                }
                return; 
            } else {
                JOptionPane.showMessageDialog(null, "Error: User not found in the database.", "Access Denied", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, "Error checking user role: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
                // TODO add your handling code here:
    }//GEN-LAST:event_btnAddBookActionPerformed

