    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        if(txtid.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"All field is required");
            txtid.requestFocus();
        }
        else if(txtname.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"All field is required");
            txtname.requestFocus();
        }
        else if(txtcourse.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"All field is required");
            txtcourse.requestFocus();
        }
        else if(txtinstitute.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"All field is required");
            txtinstitute.requestFocus();
        }
          else if(txtsem.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"All field is required");
            txtsem.requestFocus();
        }
        
          else{
            try {
                Connection con=Connect.Connection();
                PreparedStatement pst=con.prepareStatement("INSERT INTO STUDENT(id,name,department,institution,semester)values(?,?,?,?,?)");
                pst.setString(1, txtid.getText());
                pst.setString(2, txtname.getText());
                pst.setString(3, txtcourse.getText());
                pst.setString(4, txtinstitute.getText());
                pst.setString(5, txtsem.getText());
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this,"Record Saved");
                txtid.setText("");
                txtname.setText("");
                txtcourse.setText("");
                txtinstitute.setText("");
                txtsem.setText("");
                
            } catch (SQLException ex) {
                Logger.getLogger(StudentRegistration.class.getName()).log(Level.SEVERE, null, ex);
            }
              
          }
        
        // TODO add your handling code here:
    }//GEN-LAST:event_btnsaveActionPerformed

