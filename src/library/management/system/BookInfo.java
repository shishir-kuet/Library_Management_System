    private void fetchBookDetails(String bookId) {
        try {
      
            String apiUrl = "https://api.myjson.online/v1/records/0bc83ca9-9fc5-462e-abf9-0967d048e199"; 
            HttpURLConnection connection = (HttpURLConnection) new URL(apiUrl).openConnection();
            connection.setRequestMethod("GET");
            connection.setConnectTimeout(5000);  // Set timeout (optional)
            connection.setReadTimeout(5000);     // Set read timeout (optional)

            int responseCode = connection.getResponseCode();
            if (responseCode == HttpURLConnection.HTTP_OK) {  // 200 OK
                BufferedReader in = new BufferedReader(new InputStreamReader(connection.getInputStream()));
                String inputLine;
                StringBuffer response = new StringBuffer();

                while ((inputLine = in.readLine()) != null) {
                    response.append(inputLine);
                }
                in.close();

                // Parse the response as JSON
                JSONObject jsonResponse = new JSONObject(response.toString());
                JSONObject bookData = jsonResponse.getJSONObject("data").optJSONObject(bookId);

                if (bookData != null) {
                    
                    String bookTitle = bookData.getString("book_name");
                    String author = bookData.getString("author");
                    String price = bookData.getString("price");
                    String imageURL = bookData.getString("icon");
                    loadImage(imageURL);

                    // Set the details into the text fields
                    jLabel5.setText(bookTitle);
                    jLabel6.setText(author);
                    jLabel7.setText("TK " + price);
                } else {
                    JOptionPane.showMessageDialog(this, "Book not found", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Failed to fetch book details. Response code: " + responseCode, "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error fetching book details: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
